		<p>
			Los hidrácidos, también conocidos como ácidos hidrácidos o ácidos binarios, son compuestos químicos que surgen por combinación de átomos de hidrógeno con átomos de halógenos (flúor, cloro, bromo o yodo: grupo VIIA de la tabla periódica), o bien con átomos de elementos anfígenos o calcógenos, como el azufre, el selenio o el telurio (grupo VIA de la tabla). Por ejemplo: ácido clorhídrico, ácido bromhídrico, ácido yodhídrico.

			Los hidrácidos formados con halógenos actúan con estado de oxidación -1 y la fórmula que los representa es HX. Los hidrácidos formados con anfígenos actúan con estado de oxidación -2 y son esquematizados con la fórmula H2X.


			Ácido clorhídrico (HCl(ac))
			Ácido sulfhídrico (H2S(ac))
			Ácido bromhídrico (HBr(ac))
			Ácido selenhídrico (H2Se(ac))


