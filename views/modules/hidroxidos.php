		<h2>Hidroxidos</h2>
		<figure>
			<img src="views/img/hidroxidos.jpg" alt="" width="300">
		</figure>
		<p>Los hidróxidos o bases son un grupo de compuestos químicos formados por la unión entre un óxido básico (metal + O2) y H2O (agua). El hidróxido es una combinación que deriva del agua por sustitución de uno de sus átomos de hidrógeno por un metal, está presente en muchas bases.

			Antiguamente a los hidróxidos de los alcalinos y del amonio se los conocía con el nombre de álcalis, pero este término tras la implantación de la nomenclatura moderna se usa más para denominar a cualquier sustancia que presenta carácter alcalino.

			En la clasificación mineralógica de Strunz se les suele englobar dentro del grupo de los óxidos, aunque hay bibliografías que los tratan como un grupo aparte.

			Los hidróxidos se clasifican en: básicos, anfóteros y ácidos.1​ Por ejemplo, el Zn(OH)2 es un hidróxido anfótero ya que:

			con ácidos: Zn(OH)2 + 2H+ → Zn+2 + 2H2O
			con bases: Zn(OH)2 + 2OH− → [Zn(OH)4]−2</p>
