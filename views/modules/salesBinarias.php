        <p>Las sales binarias son compuestos que se forman por la unión de un elemento metálico con un elemento no metálico.4​ Su fórmula general es: MiXj donde M es el elemento metálico, i es la valencia del metal, X es el elemento no metálico y j es la valencia del no metal.

            Nomenclatura de las sales binarias
            Para nombrar las sales binarias se escribe primero el elemento no metálico añadiendo la terminación uro, posteriormente se plasma el elemento metálico.

            Por ejemplo, el sodio (Na) se combina con el flúor (F) para formar fluoruro de sodio (NaF).</p>
