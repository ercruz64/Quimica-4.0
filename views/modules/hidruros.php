        <h2>Hidruros</h2>
            <figure>
                <img src="views/img/hidruros.jpg" alt="" width="200">
            </figure>
        <p>Son compuestos formados por hidrógeno y un elemento no metálico. El no metal siempre actúa con su menor número de valencia, por lo cual cada uno de ellos forma un solo hidruro no metálico. Generalmente se encuentran en estado gaseoso a la temperatura ambiente. Algunos manifiestan propiedades ácidas, tales como los hidruros de los elementos flúor, cloro, bromo, yodo,
            azufre, selenio y telurio; mientras que otros no son ácidos, como el agua, amoníaco, metano, silanos, etc.</p>
        <p>Hidruros metálicos
            Son compuestos binarios constituidos por hidrógeno y un elemento metálico.

            Se formulan escribiendo primero el símbolo del elemento metálico.
            Se nombran con la palabra hidruro seguida del nombre del metal.
            Algunos ejemplos importantes de este tipo de hidruros son:

            NaH → hidruro de sodio
            LiH → hidruro de litio
            CaH2 → hidruro de calcio
            SrH2 → hidruro de estroncio</p>
